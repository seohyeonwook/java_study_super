package exercise.chapter_13;

public class IntegerRange {
    public static void main(String[] args) {
        // 정수
        byte myByte = 120; // 범위 - 128 ~ 127
        System.out.println(myByte);
        short myShort = 32555; // 범위 -32768 ~ 32767
        System.out.println(myShort);
        // byte < short < int < long
    }
}
